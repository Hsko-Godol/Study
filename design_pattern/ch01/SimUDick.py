import MallardDuck, RedheadDuck, RubberDuck

def main():
    mallard = MallardDuck()
    mallard.quack()
    mallard.swim()
    mallard.display()
    mallard.fly()

    redhead = RedheadDuck()
    redhead.quack()
    redhead.swim()
    redhead.display()
    redhead.fly()

    rubber = RubberDuck()
    rubber.quack()
    rubber.swim()
    rubber.display()
    rubber.fly()

if __name__ == '__main__':
    main()
